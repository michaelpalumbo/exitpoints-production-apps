// Map: "YYYY-MM-DD" -> [artistEntries...]
const artistsByDate = new Map();

artists.forEach(rawArtist => {
  const artist = { ...rawArtist };

  // If ArtistName exists and differs from Name, prefer ArtistName
  if (artist.ArtistName && artist.Name !== artist.ArtistName) {
    artist.Name = artist.ArtistName;
  }
  delete artist.ArtistName;

  // Normalize ConcertDate from MM/DD/YYYY to YYYY-MM-DD
  const isoDate = normalizeSpreadsheetDate(artist.ConcertDate);
  if (!isoDate) {
    // no usable date, skip mapping into a concert
    return;
  }

  const name = (artist.Name || "").trim();
  if (!name) {
    return;
  }

  const instagramHandle = normalizeInstagram(artist.Instagram);
  const instagramUrl = instagramHandle
    ? `https://instagram.com/${instagramHandle.replace(/^@/, "")}`
    : "";

  const entry = {
    id: slugify(name),
    name,
    instagramUrl,
    instagram: instagramHandle,
    bio: artist.Bio || "",
    images: [] // you can fill these in later if you like
  };

  if (!artistsByDate.has(isoDate)) {
    artistsByDate.set(isoDate, []);
  }
  artistsByDate.get(isoDate).push(entry);
});

