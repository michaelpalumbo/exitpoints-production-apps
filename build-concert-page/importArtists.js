// MM/DD/YYYY -> YYYY-MM-DD
function normalizeSpreadsheetDate(mmddyyyy) {
  if (!mmddyyyy) return "";
  const [month, day, year] = mmddyyyy.split("/");
  if (!year || !month || !day) return "";
  const mm = month.padStart(2, "0");
  const dd = day.padStart(2, "0");
  return `${year}-${mm}-${dd}`;
}

function slugify(name) {
  return String(name)
    .toLowerCase()
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "") // strip accents
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

// Normalize various Instagram input formats into a single @handle
// Examples handled:
// "@boreyshin"
// "boreyshin"
// "@goldzackstein and @newsymphonyorchestra"
// "https://www.instagram.com/andrewtimar/  and  https://www.instagram.com/andrew417997/"
// "Kimia_farokhzad"
function normalizeInstagram(raw) {
  if (!raw) return "";

  let str = String(raw).trim();

  if (!str) return "";

  // 1) If there are instagram URLs, pick the first username from them
  const urlRegex = /instagram\.com\/([^\/\s]+)/gi;
  const urlMatches = [];
  let m;
  while ((m = urlRegex.exec(str)) !== null) {
    if (m[1]) urlMatches.push(m[1]);
  }
  if (urlMatches.length > 0) {
    return "@" + urlMatches[0].replace(/^@/, "");
  }

  // 2) Otherwise, split on "and" / commas / whitespace and look for a token with "@"
  const tokens = str
    .split(/and|,|\s+/i)
    .map(t => t.trim())
    .filter(Boolean);

  const withAt = tokens.find(t => t.includes("@"));
  if (withAt) {
    const cleaned = withAt
      .replace(/[^@a-zA-Z0-9._]/g, "") // strip junk around
      .replace(/^@+/, "@");
    return cleaned;
  }

  // 3) Otherwise, treat the whole thing as a bare username
  return "@" + str.replace(/^@/, "");
}

// Build "Month Day" from YYYY-MM-DD (e.g. "2025-11-28" -> "November 28")
function prettyDate(isoDate) {
  const [year, month, day] = isoDate.split("-");
  const d = new Date(`${year}-${month}-${day}T00:00:00-05:00`); // offset not super important here
  return d.toLocaleDateString("en-CA", {
    month: "long",
    day: "numeric"
  });
}

// Build datetime string "YYYY-MM-DDT20:00:00-04:00"
// You can change the time and offset if needed.
function buildDatetime(isoDate) {
  return `${isoDate}T20:00:00-04:00`;
}

