function normalizeDate(mmddyyyy) {
  if (!mmddyyyy) return "";

  const [month, day, year] = mmddyyyy.split("/");

  // Ensure zero-padding
  const mm = month.padStart(2, "0");
  const dd = day.padStart(2, "0");

  return `${year}-${mm}-${dd}`;
}

